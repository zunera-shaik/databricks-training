-- Find employees with salary >= 80000
SELECT *
FROM Employees
WHERE salary >= 80000;

-- Find employees with experience <= 3
SELECT *
FROM Employees
WHERE experience <= 3;

-- Find employees whose salary <> 45000
SELECT *
FROM Employees
WHERE salary <> 45000;

-- Find employees with salary < 50000
SELECT *
FROM Employees
WHERE salary < 50000;

-- Find employees with experience > 5
SELECT *
FROM Employees
WHERE experience > 5;
